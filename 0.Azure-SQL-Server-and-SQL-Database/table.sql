CREATE TABLE MobilePhoneModels(
    Id INT NOT NULL PRIMARY KEY,
    Brand nvarchar(255),
    Model nvarchar(255),
    Color nvarchar(255),
    Memory nvarchar(255),
    Storage nvarchar(255),
    Rating nvarchar(255),
    Selling_Price nvarchar(255),
    Original_Price nvarchar(255),
    MobilePhoneDetailsID nvarchar(255)
)